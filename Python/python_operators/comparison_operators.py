# Comparison operators compare two values/variables and return a boolean result: True or False

a = 5                     # Integer value assigned to variable 'a
b = 10                    # Integer value assigned to variable 'b'

print ("a is equal to b: ", a == b) # returns False because the condition is not met (5 != 10)
print ("a is not equal to b: ", a != b) # returns True because the condition is met (5 != 10)

# The greater than operator (>) returns True if the left operand is greater than the right operand.
# It returns False if the left operand is less than or equal to the right operand.

print("Is a > b? : ", a > b) # returns False because the condition is not met (5 is not greater than 10)

# The less than operator (<) returns True if the left operand is less than the right operand.
# It returns False if the left operand is greater than or equal to the right operand.

print("Is a < b?: ", a < b) # returns True because the condition is met (5 is less than 10)