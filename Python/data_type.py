num1 = 5
print(num1, "is of type", type(num1))  # Output: 5 is of type <class 'int'>

num2 = 3.2
print(num2, "is of type", type(num2))  # Output: 3.2 is of type <class 'float'>

num3 = 1 +2j
print(num3, "is of type", type(num3))  # Output: (1+2j) is of type <class 'complex'>

