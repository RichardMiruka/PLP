void add (int num1, int num2){
  print('Sum: ${num1 + num2}');
}

// main function / running application
void main() {
  add(10, 20); // calling the add() function with parameters
}
  
  //In the above code, we have created a function called  add()  that takes two parameters  num1  and  num2 . 
  //Inside the function, we are printing the sum of the two numbers. 
  //We then call the  add()  function inside the  main()  function with two arguments  10  and  20 . 
  //When we run the application, the output will be: 
  //Sum: 30
  
